#include <string.h>
#include <stdio.h>
#include <stdlib.h>

int	find_last(char *dest)
{
	unsigned int	a;

	a = 0;
	while (dest[a] != '\0')
	{
		a++;
	}
	return (a);
}

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = find_last(dest);
	while (src[i] != '\0' && i < nb)
	{
		dest[j] = src[i];
		j++;
		i++;
	}
	dest[j] = '\0';
	return (dest);
}
